/*Программа должна спросить у пользователя число,
это будет количество денег, которое он хочет положить на счет в банке.
Затем программа должна выдать примерно такое сообщение:
"Ваша сумма в 101 рубль успешно зачислена." - в случае если пользователь ввел 101.
"Ваша сумма в 10020 рублей успешно зачислена." - в случае если пользователь ввел 10020.
"Ваша сумма в 120104 рубля успешно зачислена." - в случае если пользователь ввел 120104.
То есть ваша задача выводить слово «рубль» в правильном падеже, в зависимости от введенного числа.
*/

/**
 * Выводит сообщение о зачислении средств на счет пользователя
 *
 * @param {string} userAnswer - сколько положить на счет
 * @param {string} lastSymbol - последний символ в ответе пользователя
 * @returns {string}
 */
function amountToTheAccount(userAnswer) {
    let lastSymbol = parseInt(userAnswer.slice(-1));
    let currency = 'рубль';
    if (lastSymbol > 1 && lastSymbol < 5) {
        currency = 'рубля';
    } else if (lastSymbol === 0 || lastSymbol >= 5) {
        currency = 'рублей';
    }
    return `Ваша сумма в ${userAnswer} ${currency} успешно зачислена.`
}

let userAnswer = prompt('Введите сумму, которую хотите положить на счет');
alert(amountToTheAccount(userAnswer));

