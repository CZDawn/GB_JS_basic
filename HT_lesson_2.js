/* Реализовать основные 4 арифметические операции (+, -, /, *) в виде функций с двумя параметрами.
Т.е. например, функция для сложения должна принимать два числа, складывать их и возвращать результат.
Обязательно использовать оператор return.
*/

/**
 * Определяет сумму двух чисел
 *
 * @param {number} num1 - первое число
 * @param {number} num2 - второе число
 * @returns {number}
 */
function sumsNumbers(num1, num2) {
    return num1 + num2;
}

/**
 * Определяет разность двух чисел
 *
 * @param {number} num1 - уменьшаемое число
 * @param {number} num2 - вычитаемое число
 * @returns {number}
 */
function subtractsNumbers(num1, num2) {
    return num1 - num2;
}

/**
 * Определяет частное двух чисел
 *
 * @param {number} num1 - делимое
 * @param {number} num2 - делитель
 * @returns {number}
 */
function dividesNumbers(num1, num2) {
    return num1 / num2;
}

/**
 * Определяет произведение двух чисел
 *
 * @param {number} num1 - множимое
 * @param {number} num2 - множитель
 * @returns {number}
 */
function multipliesNumbers(num1, num2) {
    return num1 * num2;
}



