/*Реализовать функцию с тремя параметрами: function mathOperation(arg1, arg2, operation),
где arg1, arg2 – значения аргументов, operation – строка с названием операции. В зависимости от
переданного значения операции (использовать switch) выполнить одну из арифметических операций
(использовать функции из задания 4) и вернуть полученное значение.
*/

/**
 * Выполняет математические операции
 *
 * @param {number} arg1 - операнд № 1
 * @param {number} arg2 - операнд № 2
 * @param {string} operation - оператор
 * @returns {number}
 */
function mathOperation(arg1, arg2, operation) {
    switch(operation) {
        case '+':
            return 'Сумма числе равна: ' + sumsNumbers(arg1, arg2);
        case '-':
            return 'Разность чисел равна: ' + substractsNumbers(arg1, arg2);
        case '/':
            return 'Частное чисел равно: ' + dividesNumbers(arg1, arg2);
        case '*':
            return 'Произведение чисел равно: ' + multipliesNumbers(arg1, arg2);
        default:
            return 'Вы не указали оператор';
    }
}

let arg1 = parseInt(prompt('Введите первое число'));
let arg2 = parseInt(prompt('Введите второе число'));
let operation = prompt('Введите оператор для выполнение действия (+, -, /, *)');

alert(mathOperation(arg1, arg2, operation));



